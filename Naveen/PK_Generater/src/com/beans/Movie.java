package com.beans;

public class Movie {
	private int mid;
	private String mname;
	private int mseatno;
	private String mtime;

	public int getMid() {
		return mid;
	}

	public void setMid(int mid) {
		this.mid = mid;
	}

	public String getMname() {
		return mname;
	}

	public void setMname(String mname) {
		this.mname = mname;
	}

	public int getMseatno() {
		return mseatno;
	}

	public void setMseatno(int mseatno) {
		this.mseatno = mseatno;
	}

	public String getMtime() {
		return mtime;
	}

	public void setMtime(String mtime) {
		this.mtime = mtime;
	}

}
